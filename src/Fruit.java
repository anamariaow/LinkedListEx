public class Fruit {
    //creare classe Fruit con parametro String name, con costruttore e metodi getter e setter
    private String name;

    public Fruit(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return getName();
    }
}
